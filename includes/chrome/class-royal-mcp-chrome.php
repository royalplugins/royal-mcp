    public function filter_update_footer( $content ) {
        if ( ! $this->is_royal_mcp_admin_page() ) {
            return $content;
        }
        $version = defined( 'ROYAL_MCP_VERSION' ) ? ROYAL_MCP_VERSION : '';
        return sprintf(
            /* translators: %s: plugin version */
            esc_html__( 'Royal MCP v%s', 'royal-mcp' ),
            esc_html( $version )
        );
    }

    /* ==================================================================
     *  3. Royal Tools submenu page
     * ================================================================ */

    public function register_royal_tools_menu(): void {
        add_submenu_page(
            'royal-mcp',
            __( 'Royal Tools', 'royal-mcp' ),
            __( 'Royal Tools', 'royal-mcp' ),
            'manage_options',
            self::ROYAL_TOOLS_MENU_SLUG,
            [ $this, 'render_royal_tools_page' ]
        );
    }

    public function render_royal_tools_page(): void {
        if ( ! current_user_can( 'manage_options' ) ) {
            wp_die( esc_html__( 'You do not have permission to view Royal Tools.', 'royal-mcp' ) );
        }
        $family      = $this->get_family_plugins_with_state();
        $show_bundle = ! $this->founders_callout_dismissed();
        ?>
        <div class="wrap royal-mcp-tools-wrap">
            <div class="royal-mcp-tools-title-row">
                <div>
                    <h1 class="royal-mcp-tools-title"><?php esc_html_e( 'Royal Tools', 'royal-mcp' ); ?></h1>
                    <p class="royal-mcp-tools-subtitle"><?php esc_html_e( 'Free WordPress plugins from Royal Plugins — one-click install, no signup.', 'royal-mcp' ); ?></p>
                </div>
            </div>

            <?php if ( $show_bundle ) : ?>
                <?php $this->render_founders_callout(); ?>
            <?php endif; ?>

            <div class="royal-mcp-tools-section-head">
                <h2><?php esc_html_e( 'Free companion plugins', 'royal-mcp' ); ?></h2>
                <p><?php esc_html_e( 'All available on WordPress.org. Every feature ships free.', 'royal-mcp' ); ?></p>
            </div>

            <div class="royal-mcp-tools-grid">
                <?php foreach ( $family as $slug => $plugin ) : ?>
                    <?php $this->render_plugin_card( $slug, $plugin ); ?>
                <?php endforeach; ?>
            </div>
        </div>
        <?php
    }

