    /**
     * Registry + per-card state (you're here / active / installed-inactive /
     * not installed). Loaded once per page render.
     */
    public function get_family_plugins_with_state(): array {
        if ( ! function_exists( 'get_plugins' ) ) {
            require_once ABSPATH . 'wp-admin/includes/plugin.php';
        }
        $all_installed = array_keys( get_plugins() );
        $out           = $this->get_family_plugins();
        $current       = defined( 'ROYAL_MCP_PLUGIN_BASENAME' ) ? ROYAL_MCP_PLUGIN_BASENAME : 'royal-mcp/royal-mcp.php';
        foreach ( $out as $slug => &$plugin ) {
            if ( $plugin['basename'] === $current ) {
                $plugin['state'] = 'here';
            } elseif ( in_array( $plugin['basename'], $all_installed, true ) && is_plugin_active( $plugin['basename'] ) ) {
                $plugin['state'] = 'active';
            } elseif ( in_array( $plugin['basename'], $all_installed, true ) ) {
                $plugin['state'] = 'inactive';
            } else {
                $plugin['state'] = 'not_installed';
            }
        }
        return $out;
    }

    private function render_plugin_card( string $slug, array $plugin ): void {
        $state       = $plugin['state'] ?? 'not_installed';
        $card_class  = 'royal-mcp-tools-card';
        if ( 'here' === $state ) {
            $card_class .= ' royal-mcp-tools-card--here';
        } elseif ( 'active' === $state ) {
            $card_class .= ' royal-mcp-tools-card--active';
        }
        ?>
        <div class="<?php echo esc_attr( $card_class ); ?>">
            <div class="royal-mcp-tools-card-head">
                <div class="royal-mcp-tools-card-icon" aria-hidden="true"><?php echo wp_kses_post( $plugin['icon'] ); ?></div>
                <?php $this->render_card_badge( $state ); ?>
            </div>
            <h3><?php echo esc_html( $plugin['name'] ); ?></h3>
            <p><?php echo esc_html( $plugin['pitch'] ); ?></p>
            <div class="royal-mcp-tools-card-actions">
                <?php $this->render_card_actions( $slug, $plugin ); ?>
            </div>
        </div>
        <?php
    }

    private function render_card_badge( string $state ): void {
        if ( 'here' === $state ) {
            echo '<span class="royal-mcp-tools-card-badge">' . esc_html__( 'You\'re here', 'royal-mcp' ) . '</span>';
        } elseif ( 'active' === $state ) {
            echo '<span class="royal-mcp-tools-card-badge">' . esc_html__( '✓ Active', 'royal-mcp' ) . '</span>';
        } else {
            echo '<span class="royal-mcp-tools-card-badge royal-mcp-tools-card-badge--free">' . esc_html__( 'FREE', 'royal-mcp' ) . '</span>';
        }
    }

    private function render_card_actions( string $slug, array $plugin ): void {
        $state = $plugin['state'] ?? 'not_installed';
        if ( 'here' === $state ) {
            echo '<div class="royal-mcp-tools-badge-active">' . esc_html__( 'You\'re here', 'royal-mcp' ) . '</div>';
            return;
        }
        if ( 'active' === $state ) {
            echo '<div class="royal-mcp-tools-badge-active">' . esc_html__( '✓ Active', 'royal-mcp' ) . '</div>';
            return;
        }
        if ( 'inactive' === $state ) {
            $activate_url = wp_nonce_url(
                admin_url( 'plugins.php?action=activate&plugin=' . urlencode( $plugin['basename'] ) ),
                'activate-plugin_' . $plugin['basename']
            );
            printf(
                '<a href="%1$s" class="royal-mcp-tools-btn-activate">%2$s</a><a href="%3$s" target="_blank" rel="noopener noreferrer" class="royal-mcp-tools-btn-learn">%4$s</a>',
                esc_url( $activate_url ),
                esc_html__( 'Activate', 'royal-mcp' ),
                esc_url( $plugin['learn_url'] ),
                esc_html__( 'Learn', 'royal-mcp' )
            );
            return;
        }
        $install_url = admin_url(
            'plugin-install.php?s=' . rawurlencode( $plugin['search_slug'] ) . '&tab=search&type=term'
        );
        printf(
            '<a href="%1$s" class="royal-mcp-tools-btn-install">%2$s</a><a href="%3$s" target="_blank" rel="noopener noreferrer" class="royal-mcp-tools-btn-learn">%4$s</a>',
            esc_url( $install_url ),
            esc_html__( 'Install', 'royal-mcp' ),
            esc_url( $plugin['learn_url'] ),
            esc_html__( 'Learn', 'royal-mcp' )
        );
    }
