    const CSS_HANDLE                   = 'royal-mcp-chrome';
    const BODY_CLASS                   = 'royal-mcp-chrome-scope';

    /** @var self|null */
    private static $instance = null;

    public static function get_instance(): self {
        if ( null === self::$instance ) {
            self::$instance = new self();
        }
        return self::$instance;
    }

    private function __construct() {
        add_action( 'admin_menu',            [ $this, 'register_royal_tools_menu' ], 20 );
        add_action( 'in_admin_header',       [ $this, 'render_top_header' ] );
        add_filter( 'admin_footer_text',     [ $this, 'filter_admin_footer_text' ], 999 );
        add_filter( 'update_footer',         [ $this, 'filter_update_footer' ], 999 );
        add_action( 'admin_enqueue_scripts', [ $this, 'enqueue_assets' ] );
        add_filter( 'admin_body_class',      [ $this, 'filter_admin_body_class' ] );
        add_action( 'admin_post_' . self::FOUNDERS_DISMISS_ACTION, [ $this, 'handle_founders_dismiss' ] );
    }

    /* ==================================================================
     *  Screen-ID gate
     * ================================================================ */

    /**
     * True on any Royal MCP admin page (Settings, Activity Log, Royal Tools,
     * License, Wizard, etc.). Screen IDs generated by WP follow the shape
     * `toplevel_page_royal-mcp` for the parent menu, and
     * `royal-mcp_page_<submenu-slug>` for submenus.
     */
    public function is_royal_mcp_admin_page(): bool {
        if ( ! function_exists( 'get_current_screen' ) ) {
            return false;
        }
        $screen = get_current_screen();
        if ( ! $screen || empty( $screen->id ) ) {
            return false;
        }
        $id = (string) $screen->id;
        return false !== strpos( $id, 'royal-mcp' )
            || false !== strpos( $id, 'royal_mcp' );
    }

    /* ==================================================================
     *  Assets
     * ================================================================ */

    public function enqueue_assets(): void {
        if ( ! $this->is_royal_mcp_admin_page() ) {
            return;
        }
        $rel_path = 'assets/css/chrome.css';
        $abs_path = ROYAL_MCP_PLUGIN_DIR . $rel_path;
        $version  = defined( 'ROYAL_MCP_VERSION' ) ? ROYAL_MCP_VERSION : '1.0.0';
        if ( file_exists( $abs_path ) ) {
            $version .= '.' . filemtime( $abs_path );
        }
        wp_enqueue_style(
            self::CSS_HANDLE,
            ROYAL_MCP_PLUGIN_URL . $rel_path,
            [],
            $version
        );
    }

    public function filter_admin_body_class( $classes ) {
        if ( $this->is_royal_mcp_admin_page() ) {
            $classes .= ' ' . self::BODY_CLASS . ' ';
        }
        return $classes;
    }

